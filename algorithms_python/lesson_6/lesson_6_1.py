    print('Введите два шестнадцатеричных числа:')
    a = read_hex(1)                                                            # список размером по 8 бит на символ
    b = read_hex(2)                                                            # список размером по 8 бит на символ
    res = []                                                                   # список с нулевым размером
    act = input('Введите обозначение математического действия (поддерживаются '# один симовл размером 8 бит
                'все основные действия - +, -, *, /): ')
    if act == '+':
        res = dec2hex(hex2dec(a)+hex2dec(b))                                   # здесь передаются два списка, преобразуемых
    elif act == '-':                                                           # до чисел типа int. после выполнения операции,
        res = dec2hex(hex2dec(a)-hex2dec(b))                                   # происходит обратное преобразование в список
    elif act == '*':
        res = dec2hex(hex2dec(a)*hex2dec(b))
    elif act == '/':
        res = dec2hex(hex2dec(a)/hex2dec(b))
    else:
        print('Введен неверный символ математического действия')
    if len(res) > 0:                                                           # временная переменная типа int
        print('Ответ: ', ''.join(res))                                         # временная строка для вывода значения

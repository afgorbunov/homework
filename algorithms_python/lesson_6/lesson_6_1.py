def read_hex(num):
    strhex = input(f'\tчисло {str(num)}: ')                                    # создается стролка размером 8 бит на каждый символ
    err = []                                                                   # пустой список с нулевым размером
    for s in strhex:                                                           # счетчик размером 8 бит
        if (not s.isdigit()) and (ord(s) < 65 or (ord(s) > 70 and ord(s) < 97) or ord(s) > 102):
            err.append(s)                                                      # увеличивает размер списка на 8 бит за итерацию
    if len(err) > 0:
        print('Ошибка! Введены некорректные символы: "' + '", "'.join(err)+'"')# создаются временные переменные для вывода
